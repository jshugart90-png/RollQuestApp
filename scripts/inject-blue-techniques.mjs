const counts = Object.fromEntries(Object.keys(POOLS).map((k) => [k, 0]));
function nextVid(pos) {
  const pool = POOLS[pos];
  const id = pool[counts[pos] % pool.length];
  counts[pos]++;
  return id;
}

function esc(s) {
  return s.replace(/\\/g, "\\\\").replace(/"/g, '\\"');
}

function fmtTech(t) {
  const vid = nextVid(t.position);
  const steps = t.fullStepByStep.map((l) => `      "${esc(l)}"`).join(",\n");
  const tips = t.tips.map((l) => `"${esc(l)}"`).join(", ");
  const mistakes = t.commonMistakes.map((l) => `"${esc(l)}"`).join(", ");
  return `  {
    id: "${t.id}",
    name: "${esc(t.name)}",
    belt: "blue",
    position: "${t.position}",
    category: "${esc(t.category)}",
    shortDescription: "${esc(t.shortDescription)}",
    fullStepByStep: [
${steps}
    ],
    tips: [${tips}],
    commonMistakes: [${mistakes}],
    youtubeUrl: video("${vid}"),
    difficulty: "${t.difficulty}",
  }`;
}

